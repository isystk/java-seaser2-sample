    public static AdminAuthorityDiv get(String codeStr) {
		Integer code = NumberUtil.toInteger(codeStr);
		return (code != null) ? get(code.intValue()) : null;
    }

    public static AdminAuthorityDiv get(Integer adminAuthId) {
    	if (adminAuthId == null) {
    		return null;
    	}
		for (AdminAuthorityDiv value : values()) {
		    if (value.adminAuthId == adminAuthId) {
			return value;
		    }
		}
		return null;
    }

